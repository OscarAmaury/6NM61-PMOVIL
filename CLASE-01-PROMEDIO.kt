fun main() {
    //Declraramos las calificaciones.
    val Calificacion1 = 8
    val Calificacion2 = 8
    val Calificacion3 = 10

    //Hacemos la operacion para sacar el promedio.
    val Promedio = (Calificacion1 + Calificacion2 + Calificacion3) / 3.0

